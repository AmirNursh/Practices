# Strings
txt = "Hello, Python!"
print(txt)
print(txt[0])        # first character
print(txt[-1])       # last character
print(txt[0:5])      # substring

# String methods
print(txt.lower())
print(txt.upper())
print(txt.replace("Python", "World"))
print(txt.split(","))

# Concatenation
a = "Hello"
b = "World"
print(a + " " + b)
